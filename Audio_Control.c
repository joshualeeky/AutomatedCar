int tempo = 100;

void initBuzzer(void) {
	SIM_SCGC5 |= SIM_SCGC5_PORTD_MASK;
	
	PORTD->PCR[PTD0_PIN] &= ~PORT_PCR_MUX_MASK;
	PORTD->PCR[PTD0_PIN] |= PORT_PCR_MUX(4);
	
	SIM_SCGC6 |= SIM_SCGC6_TPM0_MASK;
	
	SIM->SOPT2 &= ~SIM_SOPT2_TPMSRC_MASK;
	SIM->SOPT2 |= SIM_SOPT2_TPMSRC(1);
	
	TPM0->MOD = 3276;
	
	TPM0->SC &= ~((TPM_SC_CMOD_MASK) | (TPM_SC_PS_MASK));
	TPM0->SC |= (TPM_SC_CMOD(1) | TPM_SC_PS(7));
	TPM0->SC &= ~(TPM_SC_CPWMS_MASK);
	
	TPM0_C0SC &= ~((TPM_CnSC_ELSB_MASK) | (TPM_CnSC_ELSA_MASK) | (TPM_CnSC_MSB_MASK) | (TPM_CnSC_MSA_MASK));
	TPM0_C0SC |= (TPM_CnSC_ELSB(1) | TPM_CnSC_MSB(1));
}

void pauseTune(int duration) {
		// Wait for the specified duration before playing the next note.
		TPM0->MOD = 0;
		TPM0_C0V = 0;
    osDelay((duration * tempo) / 10000);
}

// https://github.com/robsoncouto/arduino-songs/blob/master/merrychristmas/merrychristmas.ino
void playMain(int thisNote) {
	int wholenote = (60000 * 4) / tempo;
	int divider = 0, noteDuration = 0;
	
	divider = merryChristmas[thisNote + 1];
	noteDuration = (wholenote) / divider;
	
	TPM0->MOD = F2_MOD(merryChristmas[thisNote]);
	TPM0_C0V = F2_MOD(merryChristmas[thisNote]) / 2;
	osDelay((noteDuration * tempo * 50) / 10000);

	pauseTune(noteDuration);
}

// https://www.tutorialspoint.com/arduino/arduino_tone_library.htm
void playStart() {
	int wholenote = (60000 * 4) / tempo;
	int divider = 0, noteDuration = 0;
	for (int thisNote = 0; thisNote < 16; thisNote += 2) {
		divider = startupTone[thisNote + 1];
		noteDuration = (wholenote) / divider;
		
		TPM0->MOD = F2_MOD(startupTone[thisNote]);
		TPM0_C0V = F2_MOD(startupTone[thisNote]) / 2;
		osDelay((noteDuration * tempo * 34) / 10000);

		pauseTune(noteDuration);
	}
}

// https://github.com/robsoncouto/arduino-songs/blob/master/happybirthday/happybirthday.ino
void playCelebrate() {
	int notes = sizeof(endTone) / sizeof(endTone[0]);
	int wholenote = (60000 * 4) / tempo;
	int divider = 0, noteDuration = 0;
	for (int thisNote = 0; thisNote < notes; thisNote += 2) {
		divider = startupTone[thisNote + 1];
		noteDuration = (wholenote) / divider;
		
		if (divider > 0) {
      // regular note, just proceed
    noteDuration = (wholenote) / divider;
    } else if (divider < 0) {
      // dotted notes are represented with negative durations!!
      noteDuration = (wholenote) / abs(divider);
      noteDuration *= 1.5; // increases the duration in half for dotted notes
    }
		
		TPM0->MOD = F2_MOD(endTone[thisNote]);
		TPM0_C0V = F2_MOD(endTone[thisNote]) / 2;
		osDelay((noteDuration * (tempo + 5) * 3) / 1000); // Need to speed up this tune.

		pauseTune(noteDuration);
	}
}
